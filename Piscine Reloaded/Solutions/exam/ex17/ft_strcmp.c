#include <stdio.h>

int ft_strcmp(char *s1, char *s2)
{
	int i;

    while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int main(void)
{
	printf("%d", ft_strcmp("Halimon", "Halimi"));
	return (0);
}
